# esto es un modulo, para comprobar su funcionamiento vamos a utilizarlo desde la raiz ModulosPaquetes

def sumar(op1, op2):
    print("El resultado de la suma es: ", op1 + op2)


def restar(op1, op2):
    print("El resultado de la restar es: ", op1 - op2)


def multiplicar(op1, op2):
    print("El resultado de la multiplicar es: ", op1 * op2)


def potencia(base, exponente):
    print("El resultado de la elevar  es: ", base**exponente)


def redondear(numero):
    print("resultado de redondear es:", round(numero))
